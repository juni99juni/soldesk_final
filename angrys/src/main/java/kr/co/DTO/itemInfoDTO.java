package kr.co.DTO;

public class itemInfoDTO {
	
	int item_no, item_att, item_wei;
	String item_name;
	int price;
	int pk_no;
	String item_category, user_name;
	
	
	
	
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getItem_category() {
		return item_category;
	}
	public void setItem_category(String item_category) {
		this.item_category = item_category;
	}
	public int getPk_no() {
		return pk_no;
	}
	public void setPk_no(int pk_no) {
		this.pk_no = pk_no;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getItem_no() {
		return item_no;
	}
	public void setItem_no(int item_no) {
		this.item_no = item_no;
	}
	public int getItem_att() {
		return item_att;
	}
	public void setItem_att(int item_att) {
		this.item_att = item_att;
	}
	public int getItem_wei() {
		return item_wei;
	}
	public void setItem_wei(int item_wei) {
		this.item_wei = item_wei;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	
	
	

}
