package kr.co.DTO;

public class castleInfoDTO {
	
	String castle_name, castle_text;
	int castle_att, castle_def, castle_nowHP, castle_maxHP;
	
	
	public String getCastle_name() {
		return castle_name;
	}
	public void setCastle_name(String castle_name) {
		this.castle_name = castle_name;
	}
	public String getCastle_text() {
		return castle_text;
	}
	public void setCastle_text(String castle_text) {
		this.castle_text = castle_text;
	}
	public int getCastle_att() {
		return castle_att;
	}
	public void setCastle_att(int castle_att) {
		this.castle_att = castle_att;
	}
	public int getCastle_def() {
		return castle_def;
	}
	public void setCastle_def(int castle_def) {
		this.castle_def = castle_def;
	}
	public int getCastle_nowHP() {
		return castle_nowHP;
	}
	public void setCastle_nowHP(int castle_nowHP) {
		this.castle_nowHP = castle_nowHP;
	}
	public int getCastle_maxHP() {
		return castle_maxHP;
	}
	public void setCastle_maxHP(int castle_maxHP) {
		this.castle_maxHP = castle_maxHP;
	}
	
	
	
	
}

